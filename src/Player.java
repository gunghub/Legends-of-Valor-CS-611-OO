public class Player {
    public String name;
    public String pIcon;

    public Player(String name, String icon){
        this.name = name;
        this.pIcon = pIcon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getpIcon() {
        return pIcon;
    }

    public void setpIcon(String pIcon) {
        this.pIcon = pIcon;
    }


}
